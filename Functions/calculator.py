# Calculator code

def addition(int1, int2):
    return int1 + int2

def subtraction(int1, int2):
    return int1 - int2

def multiplication(int1, int2):
    return int1 * int2

def division(int1, int2):
    return int1 / int2

print(addition(6, 3))
print(subtraction(6, 3))
print(multiplication(6, 3))
print(division(6, 3))
